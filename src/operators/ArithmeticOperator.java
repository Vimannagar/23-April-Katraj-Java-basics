package operators;

public class ArithmeticOperator {
	
	public static void main(String[] args) {
		
		//addition: - '+'
		
		int i = 10;
		int j = 20;
		
		int k = i+j;
		System.out.println(k);//30
		
		//substraction:  '-'
		
						
		int l = i-j;
		System.out.println(l);//-10
		
		//Multiplication:  '*'
		
		
		int m = i*j;
		System.out.println(m);//200
		
		//Division:  '/'
		double f = 10;
		double g = 20;
		
		double n = f/g;
		System.out.println(n);//0.5
			
		// modulus '%'
			
		int h = 10;
		
		int p = 3;
		
		int w = h%p;
		
		System.out.println(w);//1
		
				
		
		
	}

}
